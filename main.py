#while loops

#while
x = 0
while x < 10:
  print(x)
  x += 1
#will output 0 - 9

print()

#If
y = 0
if y < 10:
  print(y)
  y += 1
#will out put 0 as its only exicuted once

print()

#For
for k in range(10):
  print(k)
#it will like the while output 1-10

print()

j = 4
while j > -4:
   print(j)
   j -= 1

